def combine_dict(dict1, dict2):
    combined_dict = {}

    for key in dict1:
        combined_dict[key] = dict1[key]

    for key in dict2:
        if key in combined_dict:
            combined_dict[key] += dict2[key]
        else:
            combined_dict[key] = dict2[key]
    return combined_dict

